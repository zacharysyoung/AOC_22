type score int

type shapeRule struct {
	beatenBy, beats shape
}

func getRule(x shape) shapeRule {
	switch x {
	case rock:
		// paper beats rock beats scissor
		return shapeRule{paper, scissor}
	case scissor:
		// rock beats scissor beats paper
		return shapeRule{rock, paper}
	default:
		// scissor beats paper beats rock
		return shapeRule{scissor, rock}
	}
}

func playGuide(guide []strategy, thrower func(x strategy) score) (totalScore score) {
	for _, x := range guide {
		totalScore += thrower(x)
	}
	return
}

func throwStrategy_1(x strategy) score {
	var theirs, yours shape

	switch x.col1 {
	case "A":
		theirs = rock
	case "B":
		theirs = paper
	case "C":
		theirs = scissor
	}

	switch x.col2 {
	case "X":
		yours = rock
	case "Y":
		yours = paper
	case "Z":
		yours = scissor
	}

	// fmt.Printf("them: (%s/%s), you: (%s/%s)\n", them, shapeLookup[theirs], you, shapeLookup[yours])

	var res outcome
	switch {
	case theirs == rock && yours == scissor:
		res = lose
	case theirs == scissor && yours == paper:
		res = lose
	case theirs == paper && yours == rock:
		res = lose

	case yours == rock && theirs == scissor:
		res = win
	case yours == scissor && theirs == paper:
		res = win
	case yours == paper && theirs == rock:
		res = win

	default:
		res = draw
	}

	return score(yours) + score(res)
}

func throwStrategy_2(x strategy) score {
	var theirShape shape
	var yourPlay outcome
	var yourShape shape

	switch x.col1 {
	case "A":
		theirShape = rock
	case "B":
		theirShape = paper
	case "C":
		theirShape = scissor
	}

	switch x.col2 {
	case "X":
		yourPlay = lose
	case "Y":
		yourPlay = draw
	case "Z":
		yourPlay = win
	}

	rule := getRule(theirShape)

	switch yourPlay {
	case lose:
		yourShape = rule.beats
	case draw:
		yourShape = theirShape
	case win:
		yourShape = rule.beatenBy
	}

	return score(yourShape) + score(yourPlay)
}

type strategy struct {
	col1, col2 string
}

func readGuide(input string) (guide []strategy) {
	for _, line := range strings.Split(input, "\n") {
		if line == "" {
			continue
		}
		guide = append(guide, strategy{line[0:1], line[2:3]})
	}
	return
}

